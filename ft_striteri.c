/*
Функция ft_striteri применяет функцию f к каждому символу строки s,
переданной в качестве аргумента,
и передав ее индекс в качестве первого аргумента.
Каждый символ передается по адресу f для изменения, если это необходимо.
*/
#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	index;

	index = 0;
	if (!s || !f)
		return ;
	while (*(s + index) && f)
	{
		f(index, s + index);
		index++;
	}
}
